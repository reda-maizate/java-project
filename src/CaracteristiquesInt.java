
public class CaracteristiquesInt {
	private int numChambre;
	private int numSalleDeBain;
	private int numCuisine;
	private int numSalon;
	
	public CaracteristiquesInt(int numChambre, int numSalleDeBain, int numCuisine, int numSalon) {
		this.numChambre = numChambre;
		this.numSalleDeBain = numSalleDeBain;
		this.numCuisine = numCuisine;
		this.numSalon = numSalon;
	}

	public int getNumChambre() {
		return numChambre;
	}

	public void setNumChambre(int numChambre) {
		this.numChambre = numChambre;
	}

	public int getNumSalleDeBain() {
		return numSalleDeBain;
	}

	public void setNumSalleDeBain(int numSalleDeBain) {
		this.numSalleDeBain = numSalleDeBain;
	}

	public int getNumCuisine() {
		return numCuisine;
	}

	public void setNumCuisine(int numCuisine) {
		this.numCuisine = numCuisine;
	}

	public int getNumSalon() {
		return numSalon;
	}

	public void setNumSalon(int numSalon) {
		this.numSalon = numSalon;
	}
	
	
}
